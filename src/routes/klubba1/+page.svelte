      Det är en lättviktsklubba med optimal prestanda och balans. Skaftet är tillverkat av ett unikt kolfiber som Oxdog kallar för T2500 High Pressure Carbon. Längst upp sitter deras populära knopp som bidrar till den låga vikten. 
    
